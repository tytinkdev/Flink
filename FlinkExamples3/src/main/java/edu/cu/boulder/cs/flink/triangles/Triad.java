package edu.cu.boulder.cs.flink.triangles;

public class Triad {
  public String v1;
  public String v2;
  public String v3;

  public Triad() {
    this.v1 = "";
    this.v2 = "";
    this.v3 = "";

  }

  public Triad(String v1, String v2, String v3)
  {
    this.v1 = v1;
    this.v2 = v2;
    this.v3 = v3;
  }

}
